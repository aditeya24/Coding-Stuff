public class AllDataTypes {
    public static void main(String[] args) {
        int a=10;
        float b=3.14f;
        double c=123.456;
        char d='A';
        boolean e=true;
        byte f=100;
        short g=30000;
        long h=1234567890L;

        System.out.println(a+","+b+","+c+","+d+","+e+","+f+","+g+","+h);
    }
}
